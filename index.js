function add(a, b) {
    return a + b;
}
function subtract(a, b) {
    return a - b;
}
function multiply(a, b) {
    return a * b;
}
function divide(a, b) {
    return a / b;
}
function increment(n) {
    let number = n+1
    return number;
}
function decrement(n) {
    let number = n-1
    return number;
}
function makeInt(n) {
    let number = parseInt(n, 10);
    return number;
}
function preserveDecimal(n) {
    let number = parseFloat(n);
    return number;
}